/// 内閣府が公開済みの日本の「国民の祝日・休日」。
///
/// 出典: 内閣府「昭和30年（1955年）から令和9年（2027年）国民の祝日」
/// https://www8.cao.go.jp/chosei/shukujitsu/syukujitsu.csv
///
/// 2026-07-07 時点で、内閣府ページは 2028 年分を 2027 年 2 月に掲載予定と
/// 明記している。そのため、未公表年を推測で祝日扱いしない。
String? japaneseHolidayName(DateTime date) {
  final key = date.year * 10000 + date.month * 100 + date.day;
  return _officialJapaneseHolidayNames[key];
}

bool isJapaneseHoliday(DateTime date) => japaneseHolidayName(date) != null;

